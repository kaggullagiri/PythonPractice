# indexing is the process of extracting single elements from the collection
 # syntax : var[index position]
''' Types : two types
   1. positive indexing : Extract positive index position values from left to right direction

                          Range : 0 to n-1

   2. Negative indexing : Extract negative index position values from right to left direction

                          Range: -1 to -n
Examples :
  '''
# string index

s= "giri"
print(s[0])
print(s[-2])

# list index

l=[10,20,30,40,50]
print(l[3])
print(l[-4])

# tuple index

t=100,200,300,400
print(t[1])
print(t[-1])

# in set and dictionary indexing and slicing is not possible

